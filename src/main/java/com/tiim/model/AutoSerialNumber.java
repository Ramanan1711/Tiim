package com.tiim.model;

public class AutoSerialNumber {

	private String insType;
	private String insNo;
	private String lotNumber;
	private String acceptedQty;
	private String rejectedQty;
	private String missedQty;
	private int totalQty;
	public String getInsType() {
		return insType;
	}
	public void setInsType(String insType) {
		this.insType = insType;
	}
	public String getInsNo() {
		return insNo;
	}
	public void setInsNo(String insNo) {
		this.insNo = insNo;
	}
	public String getLotNumber() {
		return lotNumber;
	}
	public void setLotNumber(String lotNumber) {
		this.lotNumber = lotNumber;
	}
	public String getAcceptedQty() {
		return acceptedQty;
	}
	public void setAcceptedQty(String acceptedQty) {
		this.acceptedQty = acceptedQty;
	}
	public String getRejectedQty() {
		return rejectedQty;
	}
	public void setRejectedQty(String rejectedQty) {
		this.rejectedQty = rejectedQty;
	}
	public String getMissedQty() {
		return missedQty;
	}
	public void setMissedQty(String missedQty) {
		this.missedQty = missedQty;
	}
	public int getTotalQty() {
		return totalQty;
	}
	public void setTotalQty(int totalQty) {
		this.totalQty = totalQty;
	}
	
	
}
